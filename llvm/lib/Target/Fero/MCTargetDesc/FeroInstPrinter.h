#ifndef LLVM_LIB_TARGET_Fero_INSTPRINTER_FeroINSTPRINTER_H
#define LLVM_LIB_TARGET_Fero_INSTPRINTER_FeroINSTPRINTER_H

#include "llvm/MC/MCInstPrinter.h"

namespace llvm {

class FeroInstPrinter : public MCInstPrinter {
public:
  FeroInstPrinter(const MCAsmInfo &MAI, const MCInstrInfo &MII,
                  const MCRegisterInfo &MRI);

  // Autogenerated by tblgen.
  std::pair<const char *, uint64_t> getMnemonic(const MCInst *MI) override;
  void printInstruction(const MCInst *MI, uint64_t Address, const MCSubtargetInfo &STI, raw_ostream &O);
  static const char *getRegisterName(unsigned RegNo);

  void printRegName(raw_ostream &OS, unsigned RegNo) const override;
  void printInst(const MCInst *MI, uint64_t Address, StringRef Annot,
                 const MCSubtargetInfo &STI, raw_ostream &O) override;

  virtual bool printAliasInstr(const MCInst *MI, uint64_t Address,
                               const MCSubtargetInfo &STI, raw_ostream &O);
  virtual void printCustomAliasOperand(const MCInst *MI, uint64_t Address,
                                       unsigned OpIdx, unsigned PrintMethodIdx,
                                       const MCSubtargetInfo &STI,
                                       raw_ostream &O);

private:
  void printOperand(const MCInst *MI, unsigned OpNo, const MCSubtargetInfo &STI, raw_ostream &O);
  void printOperand(const MCInst *MI, unsigned Address, unsigned OpNo, const MCSubtargetInfo &STI, raw_ostream &O);
  void printMemROperand(const MCInst *MI, unsigned OpNo, const MCSubtargetInfo &STI, raw_ostream &O);
  void printMemIOperand(const MCInst *MI, unsigned OpNo, const MCSubtargetInfo &STI, raw_ostream &O);
};
} // end namespace llvm

#endif // end LLVM_LIB_TARGET_Fero_INSTPRINTER_FeroINSTPRINTER_H
