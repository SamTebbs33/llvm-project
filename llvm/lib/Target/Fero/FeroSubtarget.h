#ifndef LLVM_LIB_TARGET_Fero_FeroSUBTARGET_H
#define LLVM_LIB_TARGET_Fero_FeroSUBTARGET_H

#include "FeroFrameLowering.h"
#include "FeroISelLowering.h"
#include "FeroInstrInfo.h"
#include "llvm/CodeGen/SelectionDAGTargetInfo.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"
#include "llvm/IR/DataLayout.h"
#include "llvm/MC/MCInstrItineraries.h"

#define GET_SUBTARGETINFO_HEADER
#include "FeroGenSubtargetInfo.inc"

namespace llvm {
class FeroSubtarget : public FeroGenSubtargetInfo {
protected:
  SelectionDAGTargetInfo TSInfo;
  FeroInstrInfo InstrInfo;
  FeroFrameLowering FrameLowering;
  FeroTargetLowering TLInfo;
  FeroRegisterInfo RegInfo;

public:
  /// This constructor initializes the data members to match that
  /// of the specified triple.
  FeroSubtarget(const Triple &TT, StringRef CPU, StringRef FS,
                const TargetMachine &TM);

  /// ParseSubtargetFeatures - Parses features string setting specified
  /// subtarget options.  Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef TuneCPU, StringRef FS);

  FeroSubtarget &initializeSubtargetDependencies(const Triple &TT,
                                                  StringRef CPU, StringRef FS,
                                                  const TargetMachine &TM);

  const SelectionDAGTargetInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }

  const FeroInstrInfo *getInstrInfo() const override {
    return &InstrInfo;
  }

  const TargetFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }

  const FeroRegisterInfo *getRegisterInfo() const override {
    return &RegInfo;
  }

  const FeroTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }

  /// getMaxInlineSizeThreshold - Returns the maximum memset / memcpy size
  /// that still makes it profitable to inline the call.
  unsigned getMaxInlineSizeThreshold() const {
    return 64;
  }
};
}

#endif // end LLVM_LIB_TARGET_Fero_FeroSUBTARGET_H
